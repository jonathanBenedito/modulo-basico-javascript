// Operações matemáticos

let a = 3
let b = 2

// Adição
console.log(a + b)

// Subtração
console.log(a - b)

// Multiplicação
console.log(a * b)

// Divisão
console.log(a / b)

// Módulo (Resto da divisão)
console.log(a % b)

// Concatenar valor: Usando + com diferentes variáveis irão juntar seus valores no formato string

let primeiroValor = 4
let segundoValor = "2"

console.log(primeiroValor + segundoValor)